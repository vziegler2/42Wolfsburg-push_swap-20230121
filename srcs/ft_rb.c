#include "../Header/push_swap.h"

void	ft_rb(t_stacks *s)
{
	int	tmp;
	int	i;

	i = 0;
	tmp = s->b[i];
	i++;
	while (i < s->size_b)
	{
		s->b[i - 1] = s->b[i];
		i++;
	}
	s->b[i - 1] = tmp;
	ft_printf("rb\n");
}
