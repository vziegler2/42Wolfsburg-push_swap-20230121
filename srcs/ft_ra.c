#include "../Header/push_swap.h"

void	ft_ra(t_stacks *s)
{
	int	tmp;
	int	i;

	i = 0;
	tmp = s->a[i];
	i++;
	while (i < s->size_a)
	{
		s->a[i - 1] = s->a[i];
		i++;
	}
	s->a[i - 1] = tmp;
	ft_printf("ra\n");
}
